case weather
when 'sunny'
	puts "It's a great day! Wear sunscreen and use sunglasses!"
when 'rainy'
	puts "Make sure to take your umbrella and wear rainboots!"
when 'windy'
	puts "Better use some hairspray and wear a jacket today!"
when 'snowy'
	puts "Bundle up with a hat and gloves!"
else
	puts "I don't know, you better look outside and figure it out!"
end